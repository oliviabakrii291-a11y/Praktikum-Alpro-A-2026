#Create a Class
class MyCakes:
    x = "brownies"

print (MyCakes)


#Create Object
class MyCakes:
    x = "brownies"

p1 = MyCakes()
print(p1.x)


#Delete Objects
class MyCakes:
    x = "brownies"

p1 = MyCakes()

del p1

print(p1)


#Multiple Objects
class MyCakes:
    x = "brownies"


p1 = MyCakes()
p2 = MyCakes()
p3 = MyCakes()

print(p1.x)
print(p2.x)
print(p3.x)



